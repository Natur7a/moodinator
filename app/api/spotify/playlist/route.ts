import { NextResponse } from "next/server";
import { getServerSession } from "next-auth/next";
import { authOptions } from "@/auth";
import type { MoodKey } from "@/lib/spotify";

export const dynamic = "force-dynamic";

const allowedMoods: MoodKey[] = [
  "happy",
  "sad",
  "angry",
  "surprised",
  "neutral",
];

export async function POST(request: Request) {
  const session = await getServerSession(authOptions);

  if (!session?.accessToken) {
    return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
  }

  const body = await request.json().catch(() => ({}));
  const trackUris = Array.isArray(body.tracks) ? body.tracks : [];
  const moodParam = body.mood ?? "neutral";
  const mood = allowedMoods.includes(moodParam as MoodKey)
    ? (moodParam as MoodKey)
    : "neutral";

  if (trackUris.length === 0) {
    return NextResponse.json(
      { error: "No tracks provided" },
      { status: 400 }
    );
  }

  const profileResponse = await fetch("https://api.spotify.com/v1/me", {
    headers: { Authorization: `Bearer ${session.accessToken}` },
    cache: "no-store",
  });

  if (!profileResponse.ok) {
    const data = await profileResponse.json().catch(() => ({}));
    return NextResponse.json(
      { error: data?.error?.message ?? "Failed to read profile" },
      { status: profileResponse.status }
    );
  }

  const profile = await profileResponse.json();

  const playlistResponse = await fetch(
    `https://api.spotify.com/v1/users/${profile.id}/playlists`,
    {
      method: "POST",
      headers: {
        Authorization: `Bearer ${session.accessToken}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        name: body.name ?? `Moodinator ${mood} mix`,
        description: "Generated by MOODINATOR",
        public: false,
      }),
    }
  );

  if (!playlistResponse.ok) {
    const data = await playlistResponse.json().catch(() => ({}));
    return NextResponse.json(
      { error: data?.error?.message ?? "Failed to create playlist" },
      { status: playlistResponse.status }
    );
  }

  const playlist = await playlistResponse.json();

  const addTracksResponse = await fetch(
    `https://api.spotify.com/v1/playlists/${playlist.id}/tracks`,
    {
      method: "POST",
      headers: {
        Authorization: `Bearer ${session.accessToken}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({ uris: trackUris }),
    }
  );

  if (!addTracksResponse.ok) {
    const data = await addTracksResponse.json().catch(() => ({}));
    return NextResponse.json(
      { error: data?.error?.message ?? "Failed to add tracks" },
      { status: addTracksResponse.status }
    );
  }

  return NextResponse.json({
    id: playlist.id,
    url: playlist.external_urls?.spotify,
  });
}
